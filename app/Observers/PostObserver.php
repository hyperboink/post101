     * Handle the post "created" event.
     *
     * @param  \App\Post  $post
     * @return void
     */
    public function created(Post $post)
    {
        $postUser = $post->user;

        foreach($postUser->followers as $followers){
            $followers->notify(new NewPost($post, $postUser));
        }
    }

